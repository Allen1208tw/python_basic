# 題目 4：去除重複資料
#
# 給定一個 list，請輸出不重複的數字。
#
# 範例資料：
# nums = [1, 2, 2, 3, 4, 4, 5, 1]
#
# 要求：
# 1. 輸出不重複的數字。
# 2. 請嘗試至少一種做法。
#
# 挑戰：
# - 做法一：使用 set 去除重複資料。
# - 做法二：使用 for 迴圈與新的 list，並保留原本順序。
#
# 提示：
# - set(nums) 可以快速去除重複資料，但順序不一定和原本相同。
# - 如果要保留順序，可以建立 empty list，逐一檢查數字是否已存在。

nums = [1, 2, 2, 3, 4, 4, 5, 1]
x = set(nums)
print(x)
y = []
for i in nums:
    if i not in y:
        y.append(i)
print(y)